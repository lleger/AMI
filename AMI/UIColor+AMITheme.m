#import "UIColor+AMITheme.h"

@implementation UIColor (AMITheme)

+ (UIColor *)AMIRedColor
{
    return [UIColor colorWithRed:0.894 green:0.141 blue:0.094 alpha:1.000];
}

+ (UIColor *)AMIGreyColor
{
    return [UIColor colorWithWhite:0.141 alpha:1.000];
}

@end
